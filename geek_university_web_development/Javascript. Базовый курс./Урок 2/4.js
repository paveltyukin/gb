export const calculateSumOfNumbers = (number1, number2) => {
    return number1 + number2
};

export const calculateSubtractionOfNumbers = (number1, number2) => {
    return number1 - number2
};

export const calculateMultiplicationOfNumbers = (number1, number2) => {
    return number1 * number2;
};

export const calculateDivisionOfNumbers = (number1, number2) => {
    return number1 / number2;
};